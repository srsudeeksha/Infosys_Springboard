# Built-in functions use cases and advantages

# len() -> find length of string/list
name = "Infosys"
print("Length of string:", len(name))

# max() and min() -> find largest/smallest element
numbers = [3, 7, 1, 9, 2]
print("Maximum:", max(numbers))
print("Minimum:", min(numbers))

# sum() -> sum of all numbers in list
print("Sum of numbers:", sum(numbers))

# sorted() -> returns sorted list
print("Sorted numbers:", sorted(numbers))

# type() -> returns type of a variable
print("Type of name:", type(name))

# Advantages:
# 1. Saves time (no need to write logic manually)
# 2. Optimized performance
# 3. Improves code readability
# 4. Useful for quick problem-solving